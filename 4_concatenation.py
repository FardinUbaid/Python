# Simple
print("Hi! " + "This is Fardin")

# using variables
greetings = "Hi! "
name = "Fardin"

print(greetings + name)
# note that + must be used for same type of datatype only
# with extra texts
print(greetings + "This is " + name)

# we can use (,) too. Comma adds a blank space for each use .
print(greetings,"this is",name)